    private void SubmitExpenseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitExpenseButtonActionPerformed
        BigDecimal amount = null;
        try {
            amount = new BigDecimal(AmountText.getText());
                   } catch (Exception expobj){
            JOptionPane.showMessageDialog(this, "Amount seems wrong..", "Oops", JOptionPane.PLAIN_MESSAGE);
            AmountText.setSelectionStart(0);
            AmountText.setSelectionEnd(AmountText.getText().length());
            return;
        }
        
        BudgetCategory b;
        if (CategoryDropDown.getSelectedIndex() > 0)
            b = bc.get(CategoryDropDown.getSelectedIndex()-1);
        else
            b = ubc;
        try {
            LogicController.setExpenseBudgetCategory(exp, b);
            LogicController.setExpenseTitle(exp,DescriptionText.getText());
            LogicController.setExpenseCost(exp,amount.multiply(new BigDecimal(""+100)).toBigInteger().longValue());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage()+"..", "Oops", JOptionPane.PLAIN_MESSAGE);             
            return;
        }
        
        this.dispose();
    }//GEN-LAST:event_SubmitExpenseButtonActionPerformed

