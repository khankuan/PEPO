    public EditExpense(java.awt.Frame parent, boolean modal, Expense ex) {
        super(parent, modal);
        initComponents();
        exp = ex;
        bc = exp.getBudgetCategory().getEvent().getBudgetCategoryList();
        ubc = exp.getBudgetCategory().getEvent().getUncategorizedBudget();
        int at = 0;
        CategoryDropDown.addItem("None");
        for (int i = 0; i < bc.size(); i++){
            CategoryDropDown.addItem(bc.get(i).getName());
            if (bc.get(i) == ex.getBudgetCategory())
                at = i+1;
        }
        CategoryDropDown.setSelectedIndex(at);
        DescriptionText.setText(exp.getTitle());
        AmountText.setText((new BigDecimal(exp.getCost())).divide(new BigDecimal("100")).toString());
    }

